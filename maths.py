# Built-in math functions 
# min() and max()

x = min(3, 6, 8, 1)
y = max(6, 7, 9,23)
print(x)
print(y)

# abs() function returns the absolute (positive) values

a = abs(-6)
print(a)

# pow(x, y) function returns the values of x to the the power of y (xy).

x = pow(5, 7)
print(x)

# The Math Module 

# math.sqrt()

import math
x = math.sqrt(65)
print(x)

# math.ceil() method ounds a number upwards to its nearest integer
# math.floor() method rounds a number downwards to its nearest integer

x = math.ceil(4.7)
y = math.floor(6.8)

print(x)
print(y)

# math.pi 

x = math.pi

print(x)


