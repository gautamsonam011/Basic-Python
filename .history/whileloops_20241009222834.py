# Python Loops:- 

# python has two primitive loop commands: 
# while loops 
# for loops 

# The while loop: =>  With the while loop we can execute a set of statements as long as a condition is true

i = 1
while i < 8:
    print(i)
    i += 1

# The break Statement 

i = 1
while i < 10:
    print(i)
    if i == 5:
        break
    i += 1