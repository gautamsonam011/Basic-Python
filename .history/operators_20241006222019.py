# (1):- Arithmetic Operators 
# Addition (+) 

x = 9
y = 3
z = x+y
print(z)

# Subtraction (-) 

z = x - y
print(z)

# Multiplication (*) 

z = x * y
print(z)

# Division (/) 

z = x/y
print(z)

# Modulus (%) 

z = x % y
print(z)

# Exponentiation (**)

z = x ** y
print(z)

# Floor division (//)

z = x // y

print(z)

# Python Assignment Operators 

# (=)
x = 8
print(x)

# (+=) 

x += 8  # x = x+8
print(x)

# (-=)

x -= 7 # x = x - 7
print(x)

# (*=)

x *= 4 # x = x * 4
print(x)

# (/=)

x /= 3 # x = x / 3
print(x)

# (%=)

x %= 3 # x = x % 3
print(x)